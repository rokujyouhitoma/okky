		GameObject mainCamera;
		GameObject layer;
		GameObject ground;

		public float player1Life;
		public float player2Life;

		public float NinjaWeight;
		public float RedNinjaWeight;

		enum PlayerID {
			Player1 = 1,
			Player2 = 2,
		}

		void Awake() {
			mainCamera = GameObject.Find("/Camera");
			layer = GameObject.Find("/Layer");
			ground = GameObject.Find("/Layer/Background");
			generator = GetComponent<Generator>();
			SetupMapChip(mapchipBackground, ground);
			SetupMapChip(mapchipBlocks, layer);
			SetupMapChip(mapchipItemAndNinja, layer);
			InvokeRepeating("SendInNinja", 2f, 2f);
		}

		void Start() {
		}

		void Update() {
			if (!ExistKoban()) {
				AreaClear ();
			}
			if (!ExistNinja()) {
				SendInNinja();
			}
			if (IsLifeZeroByPlayerId((int)PlayerID.Player1) && IsLifeZeroByPlayerId((int)PlayerID.Player2)) {
				ResetGame();
			}
		}

		public float GetPlayerLife(int playerId) {
			float lives = 0f;
			switch(playerId) {
			case (int)PlayerID.Player1:
				lives = player1Life;
				break;
			case (int)PlayerID.Player2:
				lives = player2Life;
				break;
			}
			return lives;
		}

		bool IsLifeZeroByPlayerId(int playerId) {
			switch(playerId) {
			case (int)PlayerID.Player1: 
				if (player1Life <= 0) {
					return true;
				}
				break;
			case (int)PlayerID.Player2:
				if (player2Life <= 0) {
					return true;
				}
				break;
			}
			return false;
		}

		public void DamageLife(int playerId, float amount) {
			switch(playerId) {
			case (int)PlayerID.Player1:
				if (0f < player1Life) {
					player1Life = Mathf.Max(0f, player1Life - amount);
				}
				break;
			case (int)PlayerID.Player2:
				if (0f < player2Life) {
					player2Life = Mathf.Max(0f, player2Life - amount);
				}
				break;
			}
		}

		void SetupMapChip(int[][] mapchip, GameObject parent) {
			var layerTransform = layer.GetComponent<RectTransform>();
			float viewWidth = layerTransform.rect.width;
			float viewHeight = layerTransform.rect.height;
			float chipWidth = 8f; //TODO
			float chipHeight = 8f; //TODO
			int y = 0;
			int x = 0;
			for(y = 0 ; y < mapchip.Length; ++y) {
				for(x = 0; x < mapchip[y].Length; ++x) {
					var chipId = mapchip[y][x];
					if (chipId != 0) {
						var prefab = generator.GenerateById(chipId);
						if (prefab != null) {
							var position = new Vector3(- viewWidth/2 + chipWidth/2 + chipWidth * (x),
							                           viewHeight/2 - chipHeight/2 - chipHeight * (y));
							var chip = (GameObject)Instantiate(prefab, Vector3.zero, Quaternion.identity);
							chip.transform.parent = parent.transform;
							chip.transform.position = position;
						}
					}
				}
			}
		}

		void ResetGame() {
			Debug.Log ("ResetGame");
			Application.LoadLevel("GameMain");
		}

